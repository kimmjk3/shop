	ATTACH_THUMBNAIL_SIZE              NUMBER          NULL,
	ATTACH_THUMBNAIL_LOCATION          VARCHAR(200)    NULL,
	ATTACH_CONTENTS_ORIGINAL_NAME      VARCHAR(200)    NULL,
	ATTACH_CONTENTS_SAVE_NAME          VARCHAR(200)    NULL,
	ATTACH_CONTENTS_SIZE               NUMBER          NULL,
	ATTACH_CONTENTS_LOCATION           VARCHAR(200)    NULL,
	ATTACH_INSERT_DATE                 DATE            NULL,
	ATTACH_DELETE_DATE                 DATE            NULL,
	PRODUCT_NUMBER                     NUMBER          NOT NULL

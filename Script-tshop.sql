-- 1. TB_USER_INFO 테이블
CREATE TABLE TSHOP.TB_USER_INFO(
	  USER_ID VARCHAR(20) NOT NULL
	, USER_PW VARCHAR(20) NOT NULL
	, USER_NAME VARCHAR(20) NULL
	, USER_GENDER VARCHAR(20) NULL -- 1:남성 2:여성
	, USER_DOB VARCHAR(20) NULL
	, USER_ADDRESS1 VARCHAR(20) NULL -- 우편번호
	, USER_ADDRESS2 VARCHAR(20) NULL -- 기본주소
	, USER_ADDRESS3 VARCHAR(20) NULL -- 상세주소
	, USER_PHONE VARCHAR(20) NULL
	, USER_EMAIL VARCHAR(20) NULL
	, USER_AUTHORITY VARCHAR(20) NULL	-- 0:관리자 1:유저
	, USER_STATE VARCHAR(20) NULL -- 0:탈퇴 1:활동중 2:정지
	, USER_REGISTER_DT DATE NULL
	, USER_UPDATE_DT DATE NULL
);
ALTER TABLE TSHOP.TB_USER_INFO ADD CONSTRAINT PK_TB_USER_INFO PRIMARY KEY (USER_ID);

-- 2. BOARD 테이블
CREATE TABLE TSHOP.TB_BOARD_INFO (
	  BOARD_NUMBER            NUMBER          NOT NULL
	, BOARD_NAME              VARCHAR(20)     NULL
	, BOARD_INPUT_DATE        DATE DEFAULT SYSDATE NULL
	, BOARD_CORRECT           DATE            NULL
	, BOARD_DELETE_DATE       DATE            NULL
);
ALTER TABLE TSHOP.TB_BOARD_INFO ADD CONSTRAINT PK_TB_BOARD_INFO PRIMARY KEY (BOARD_NUMBER);

-- 3. POST 테이블
CREATE TABLE TSHOP.TB_POST_INFO (
	POST_NUMBER             NUMBER          NOT NULL,
	BOARD_NUMBER            NUMBER          NOT NULL,
	POST_TITLE              VARCHAR(100)    NOT NULL,
	POST_CONTENTS           VARCHAR(1000)   NOT NULL,
	POST_CATEGORY           NUMBER          NOT NULL, -- 1: 마우스 2:키보드
	POST_SCORE              NUMBER          NOT NULL,
	POST_INPUT_DATE         DATE DEFAULT SYSDATE NOT NULL,
	POST_CORRECT            DATE            NULL,
	POST_DELETE_DATE        DATE            NULL,
	POST_RECOMMEND          NUMBER DEFAULT 0 NOT NULL,
	USER_ID                 VARCHAR(20)     NOT NULL
);
ALTER TABLE TSHOP.TB_POST_INFO ADD CONSTRAINT PK_TB_POST PRIMARY KEY (POST_NUMBER);
COMMENT ON COLUMN TSHOP.TB_POST_INFO.POST_CATEGORY IS '1: 마우스 2:키보드';

-- 4. TB_COMMENT 테이블
CREATE TABLE TSHOP.TB_COMMENT_INFO (
	COMMENT_NUMBER          NUMBER          NOT NULL,
	POST_NUMBER             NUMBER          NOT NULL,
	BOARD_NUMBER            NUMBER          NOT NULL,
	COMMENT_CONTENTS        VARCHAR(1000)   NOT NULL,
	COMMENT_INPUT_DATE      DATE DEFAULT SYSDATE NOT NULL,
	COMMENT_CORRECT         DATE            NULL,
	COMMENT_DELETE_DATE     DATE            NULL,
	COMMENT_RECOMMEND       NUMBER DEFAULT 0 NOT NULL,
	USER_ID                 VARCHAR(20)     NOT NULL
);
ALTER TABLE TSHOP.TB_COMMENT_INFO ADD CONSTRAINT PK_TB_COMMENT PRIMARY KEY (COMMENT_NUMBER, POST_NUMBER, BOARD_NUMBER);

-- 5. PRODUCT 테이블
CREATE TABLE TSHOP.TB_PRODUCT_INFO (
	PRODUCT_NUMBER          NUMBER          NOT NULL,
	PRODUCT_CATEGORY        VARCHAR(100)     NOT NULL,
	PRODUCT_STATE           NUMBER          NOT NULL, -- 0:준비중 1:판매중
	PRODUCT_NAME            VARCHAR(100)     NOT NULL,
	PRODUCT_PRICE           NUMBER          NOT NULL,
	PRODUCT_STOCK           NUMBER          NULL,
	PRODUCT_DESC            VARCHAR(2000)   NOT NULL,
	PRODUCT_HITS            NUMBER DEFAULT 0 NOT NULL,
	PRODUCT_DATE            DATE DEFAULT SYSDATE NOT NULL,
	PRODUCT_CORRECT         DATE            NULL,
	PRODUCT_DELETE_DATE     DATE            NULL,
	USER_ID                 VARCHAR(20)     NOT NULL
);
ALTER TABLE TSHOP.TB_PRODUCT_INFO ADD CONSTRAINT PK_PRODUCT PRIMARY KEY (PRODUCT_NUMBER);
COMMENT ON COLUMN TSHOP.TB_PRODUCT_INFO.PRODUCT_STATE IS '0:준비중 1:판매중';

-- 6. TB_USER_ORDER 테이블
CREATE TABLE TSHOP.TB_USER_ORDER (
	ORDER_NUMBER            NUMBER          NOT NULL,
	ORDER_DATE              DATE DEFAULT SYSDATE NOT NULL,
	DELIVERY_ADDRESS1       VARCHAR(20)     NOT NULL,
	DELIVERY_ADDRESS2       VARCHAR(20)     NOT NULL,
	DELIVERY_ADDRESS3       VARCHAR(20)     NOT NULL,
	DELIVERY_MESSAGE        VARCHAR(100)    NULL,
	RECEIVER_NAME           VARCHAR(20)     NOT NULL,
	RECEIVER_PHONE          VARCHAR(20)     NOT NULL,
	USER_ID                 VARCHAR(20)     NOT NULL
);
ALTER TABLE TSHOP.TB_USER_ORDER ADD CONSTRAINT PK_TB_USER_ORDER PRIMARY KEY (ORDER_NUMBER);

-- 7. TB_USER_ORDER_DETAIL 테이블
CREATE TABLE TSHOP.TB_USER_ORDER_DETAIL (
	ORDER_DETAIL_NUMBER     NUMBER          NOT NULL,
	ORDER_COUNT             NUMBER          NULL,
	ORDER_STATE             NUMBER          NULL,
	ORDER_NUMBER            NUMBER          NOT NULL,
	PRODUCT_NUMBER          NUMBER          NOT NULL
);
ALTER TABLE TSHOP.TB_USER_ORDER_DETAIL ADD CONSTRAINT PK_TB_USER_ORDER_DETAIL PRIMARY KEY (ORDER_DETAIL_NUMBER);

-- 8. INTEREST_ITEM 테이블
CREATE TABLE TSHOP.TB_INTEREST_ITEM (
	INTEREST_ITEMNUMBER     NUMBER          NOT NULL,
	USER_ID                 VARCHAR(20)     NOT NULL,
	PRODUCT_NUMBER          NUMBER          NOT NULL
	, DELETE_YN VARCHAR(20) NULL
	, REGISTER_DT DATE NULL
	, UPDATE_DT DATE NULL
);
ALTER TABLE TSHOP.TB_INTEREST_ITEM ADD CONSTRAINT PK_TB_INTEREST_ITEM PRIMARY KEY (INTEREST_ITEMNUMBER, USER_ID);

-- 9. TB_PRODUCT_IMAGEFILE 테이블 (BOARD_NUMBER 추가됨)
